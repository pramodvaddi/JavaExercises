class DataTypes {
    public static void main(String[] args) {
        //Numerical type - Whole number.
        byte age = 12;
        short rollNumber = 2898;
        int mobileNumber = 98898989;
        long population = 98898982432398L;

        //Numerical type - Decimal number.
        float percentage = 98.4F;
        double pieValue = 3.141;

        //Boolean type
        boolean isPassed = true;
        boolean isBritish = false;

        //Char type
        char letter = 'd';

        //String type
        String name = "Pramod";

        // Printing all the variable types
        System.out.println(age);
        System.out.println(rollNumber);
        System.out.println(mobileNumber);
        System.out.println(population);
        System.out.println(percentage);
        System.out.println(pieValue);
        System.out.println(isPassed);
        System.out.println(isBritish);
        System.out.println(letter);
        System.out.println(name);

        System.out.println("My name is " + name + " I am " + age + " years old. My percentage is " + percentage);






    }
}